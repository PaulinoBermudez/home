public class EstructuraRepetitivaWhile8 {
    public static void main(String[] args) {
        int x,termino;
        x=1;
        termino=11;
        while (x<=25) {
            System.out.print(termino);
            System.out.print(" - ");
            x=x + 1;
            termino=termino +11;
        }
    }
}
